import { GET_POSTS, GET_POSTS_ERR } from '../actionTypes/postsType'
import axios from "axios";


// GET POSTS ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
export const getPosts = payload => async dispatch => {
    try {
        let postsData = await axios.get('https://jsonplaceholder.typicode.com/posts')
        let data = postsData && postsData.data
        dispatch({
            type: GET_POSTS,
            payload: data || [],
        });
    } catch (error) {
        dispatch({
            type: GET_POSTS_ERR,
            payload: error.message
        });
    }
}